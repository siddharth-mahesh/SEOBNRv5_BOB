REAL8 compute_v4P_Hreal(REAL8 m1,REAL8 m2,INT tortoise,REAL8 x,REAL8 y,REAL8 z,REAL8 p1,REAL8 p2,REAL8 p3,REAL8 S1x,REAL8 S1y,REAL8 S1z,REAL8 S2x,REAL8 S2y,REAL8 S2z){
    REAL8 EMgamma = 0.577215664901532860606512090082402431 ;
    REAL8 M=m1+m2 ;
    REAL8 mu=m1*m2/M ;
    REAL8 eta=mu/M ;
    REAL8 r=sqrt(x*x+y*y+z*z) ;
    REAL8 u=1/r ;
    REAL8 sigmastar3=m2/m1*S1z+m1/m2*S2z ;
    REAL8 sigmastar2 = m2/m1*S1y + m1/m2*S2y ;
    REAL8 sigmastar1 = m2/m1*S1x + m1/m2*S2x ;
    REAL8 sigma3=S1z+S2z ;
    REAL8 sigma2 = S1y + S2y ;
    REAL8 sigma1 = S1x + S2x ;
    REAL8 Skerr3=sigma3 ;
    REAL8 Skerr2 = sigma2 ;
    REAL8 Skerr1 = sigma1 ;
    REAL8 Skerrmag=sqrt(Skerr1*Skerr1+Skerr2*Skerr2+Skerr3*Skerr3) ;
    REAL8 Skerrhat3=Skerr3/Skerrmag ;
    REAL8 Skerrhat2 = Skerr2/Skerrmag ;
    REAL8 Skerrhat1 = Skerr1/Skerrmag ;
    REAL8 a=Skerrmag ;
    REAL8 L3=x*p2-y*p1 ;
    REAL8 L2 = z*p1 - x*p3 ;
    REAL8 L1 = y*p3 - z*p2 ;
    REAL8 Lnorm=sqrt(L1*L1+L2*L2+L3*L3) ;
    REAL8 Lhat3=L3/Lnorm ;
    REAL8 Lhat2 = L2/Lnorm ;
    REAL8 Lhat1 = L1/Lnorm ;
    REAL8 S2dotLhat=S2x*Lhat1+S2y*Lhat2+S2z*Lhat3 ;
    REAL8 S1dotLhat = S1x*Lhat1 + S1y*Lhat2 + S1z*Lhat3 ;
    REAL8 S1perp3=S1z-S1dotLhat*Lhat3 ;
    REAL8 S1perp2 = S1y - S1dotLhat*Lhat2 ;
    REAL8 S1perp1 = S1x - S1dotLhat*Lhat1 ;
    REAL8 S2perp3=S2z-S2dotLhat*Lhat3 ;
    REAL8 S2perp2 = S2y - S2dotLhat*Lhat2 ;
    REAL8 S2perp1 = S2x - S2dotLhat*Lhat1 ;
    REAL8 Sperp3=S1perp3+S2perp3 ;
    REAL8 Sperp2 = S1perp2 + S2perp2 ;
    REAL8 Sperp1 = S1perp1 + S2perp1 ;
    REAL8 n3=z/r ;
    REAL8 n2 = y/r ;
    REAL8 n1 = x/r ;
    REAL8 e33=Skerrhat3 ;
    REAL8 e32 = Skerrhat2 ;
    REAL8 e31 = Skerrhat1 ;
    REAL8 xi3=e31*n2-e32*n1 ;
    REAL8 xi2 = -e31*n3 + e33*n1 ;
    REAL8 xi1 = e32*n3 - e33*n2 ;
    REAL8 v3=n1*xi2-n2*xi1 ;
    REAL8 v2 = n3*xi1 - n1*xi3 ;
    REAL8 v1 = n2*xi3 - n3*xi2 ;
    REAL8 costheta=e31*n1+e32*n2+e33*n3 ;
    REAL8 sin2theta=1-costheta*costheta ;
    REAL8 xisq = sin2theta ;
    REAL8 w2=a*a+r*r ;
    REAL8 Sigma=r*r+a*a*costheta*costheta ;
    REAL8 Dinv=1+log(1+6*eta*u*u+2*(26-3*eta)*eta*u*u*u) ;
    REAL8 omegatilde=2*a*r ;
    REAL8 chi=(Skerr1*Lhat1+Skerr2*Lhat2+Skerr3*Lhat3)/(1-2*eta)+np.divide(1,2)*(Sperp1*Skerr1+Sperp2*Skerr2+Sperp3*Skerr3)/(Skerrmag*(1.-2.*eta)) ;
    REAL8 Kchi0=267.788*eta*eta*eta-126.687*eta*eta+10.2573*eta+1.7336 ;
    REAL8 K=-59.1658*chi*chi*chi*eta*eta*eta-0.426958*chi*chi*chi*eta+1.43659*chi*chi*chi+31.1746*chi*chi*eta*eta*eta+6.16466*chi*chi*eta*eta-1.38086*chi*chi-27.5201*chi*eta*eta*eta+17.3736*chi*eta*eta+2.26831*chi*eta-1.62045*chi+Kchi0 ;
    REAL8 etaKminus1 = eta*K - 1 ;
    REAL8 Delta0=K*(eta*K-2) ;
    REAL8 Delta1=-2*etaKminus1*(K+Delta0) ;
    REAL8 Delta2=np.divide(1,2)*Delta1*(Delta1-4*etaKminus1)-a*a*etaKminus1*etaKminus1*Delta0 ;
    REAL8 Delta3=-np.divide(1,3)*Delta1*Delta1*Delta1+etaKminus1*Delta1*Delta1+Delta2*Delta1-2*etaKminus1*(Delta2-etaKminus1)-a*a*etaKminus1*etaKminus1*Delta1 ;
    REAL8 Delta4=np.divide(1,12)*(6*a*a*(Delta1*Delta1-2*Delta2)*etaKminus1*etaKminus1+3*Delta1*Delta1*Delta1*Delta1-8*etaKminus1*Delta1*Delta1*Delta1-12*Delta2*Delta1*Delta1+12*(2*etaKminus1*Delta2+Delta3)*Delta1+12*(np.divide(94,3)-np.divide(41,32)*M_PI*M_PI)*etaKminus1*etaKminus1+6*(Delta2*Delta2-4*Delta3*etaKminus1)) ;
    REAL8 Delta5=etaKminus1*etaKminus1*(np.divide(-4237,60)+np.divide(128,5)*EMgamma+np.divide(2275,512)*M_PI*M_PI-np.divide(1,3)*a*a*(Delta1*Delta1*Delta1-3*Delta1*Delta2+3*Delta3)-(Delta1*Delta1*Delta1*Delta1*Delta1-5*Delta1*Delta1*Delta1*Delta2+5*Delta1*Delta2*Delta2+5*Delta1*Delta1*Delta3-5*Delta2*Delta3-5*Delta1*Delta4)/(5*etaKminus1*etaKminus1)+(Delta1*Delta1*Delta1*Delta1-4*Delta1*Delta1*Delta2+2*Delta2*Delta2+4*Delta1*Delta3-4*Delta4)/(2*etaKminus1)+np.divide(256,5)*log(2)+(np.divide(41,32)*M_PI*M_PI-np.divide(221,6))*eta) ;
    REAL8 Delta5l=etaKminus1*etaKminus1*np.divide(64,5) ;
    REAL8 logarg=u*(Delta1+u*(Delta2+u*(Delta3+u*(Delta4+u*(Delta5+Delta5l*log(u)))))) ;
    REAL8 Deltaucalib = 1 + eta*(Delta0 + log1p( logarg)) ;
    REAL8 Deltaucalibprime=-eta*u*u*(Delta1+u*(2*Delta2+u*(3*Delta3+u*(4*Delta4+u*(5*Delta5+Delta5l*(1+5*log(u)))))))/(1+logarg) ;
    REAL8 Deltaubar=a*a*u*u+(2*u+1/etaKminus1)/etaKminus1 ;
    REAL8 Deltaubarprime = -2*a*a*u*u*u - 2*u*u/(etaKminus1) ;
    REAL8 Deltau=fabs(Deltaubar*Deltaucalib) ;
    REAL8 Deltauprime = Deltaubarprime*Deltaucalib + Deltaubar*Deltaucalibprime ;
    REAL8 Deltatprime=2*r*Deltau+r*r*Deltauprime ;
    REAL8 Deltat=r*r*Deltau ;
    REAL8 Deltar=Deltat*Dinv ;
    REAL8 Lambdat=fabs(w2*w2-a*a*Deltat*sin2theta) ;
    REAL8 csi=sqrt(Deltar*Deltat)/w2 ;
    REAL8 csi1=1+(1-fabs(1-(REAL8)tortoise))*(csi-1) ;
    REAL8 csi2=1+(np.divide(1,2)-np.divide(1,2)*copysign(np.divide(3,2)-(REAL8)tortoise))*(csi-1) ;
    REAL8 prT=csi2*(p1*n1+p2*n2+p3*n3) ;
    REAL8 phat3=p3-prT*(1-1/csi1)*n3 ;
    REAL8 phat2 = p2 - prT*(1 - 1/csi1)*n2 ;
    REAL8 phat1 = p1 - prT*(1 - 1/csi1)*n1 ;
    REAL8 pdotxir=(phat1*xi1+phat2*xi2+phat3*xi3)*r ;
    REAL8 pdotn=phat1*n1+phat2*n2+phat3*n3 ;
    REAL8 pdotvr=(phat1*v1+phat2*v2+phat3*v3)*r ;
    REAL8 pphi=pdotxir ;
    REAL8 Qcoeff2=1/(Sigma*sin2theta) ;
    REAL8 Qcoeff1=Sigma/(Lambdat*sin2theta) ;
    REAL8 DrSipn2=Deltar*pdotn*pdotn/Sigma ;
    REAL8 Q=1+DrSipn2+Qcoeff1*pdotxir*pdotxir+Qcoeff2*pdotvr*pdotvr ;
    REAL8 Qminus1 = Q - 1 ;
    REAL8 Jtilde=sqrt(Deltar) ;
    REAL8 exp2mu=Sigma ;
    REAL8 expmu = sqrt(exp2mu) ;
    REAL8 Brtilde=(sqrt(Deltar)*Deltatprime-2*Deltat)/(2*sqrt(Deltar*Deltat)) ;
    REAL8 Btilde=sqrt(Deltat) ;
    REAL8 exp2nu=Deltat*Sigma/Lambdat ;
    REAL8 expnu = sqrt(exp2nu) ;
    REAL8 omega=omegatilde/Lambdat ;
    REAL8 omegatildeprime=2*a ;
    REAL8 Lambdatprime=4*(a*a+r*r)*r-a*a*Deltatprime*sin2theta ;
    REAL8 mucostheta=a*a*costheta/Sigma ;
    REAL8 nucostheta=a*a*w2*costheta*(w2-Deltat)/(Lambdat*Sigma) ;
    REAL8 omegacostheta=-2*a*a*costheta*Deltat*omegatilde/(Lambdat*Lambdat) ;
    REAL8 mur=r/Sigma-1/sqrt(Deltar) ;
    REAL8 nur=r/Sigma+w2*(w2*Deltatprime-4*r*Deltat)/(2*Lambdat*Deltat) ;
    REAL8 omegar=(Lambdat*omegatildeprime-Lambdatprime*omegatilde)/(Lambdat*Lambdat) ;
    REAL8 dSO=147.481*chi*chi*chi*eta*eta-568.651*chi*chi*chi*eta+66.1987*chi*chi*chi-343.313*chi*chi*eta+2495.29*chi*eta*eta-44.5324 ;
    REAL8 sigmacoeffTerm3 = eta*dSO*u*u*u ;
    REAL8 sigmacoeffTerm2=eta/(144*r*r)*(-896+r*(-436*Qminus1-96*DrSipn2+r*(-45*Qminus1*Qminus1+36*Qminus1*DrSipn2))+eta*(-336+r*(204*Qminus1-882*DrSipn2+r*(810*DrSipn2*DrSipn2-234*Qminus1*DrSipn2)))) ;
    REAL8 sigmacoeffTerm1=eta/12*(-8/r+3*Qminus1-36*DrSipn2) ;
    REAL8 sigmacoeff=sigmacoeffTerm1+sigmacoeffTerm2+sigmacoeffTerm3 ;
    REAL8 sigmastarcoeffTerm2=eta/(72*r*r)*(706+r*(-206*Qminus1+282*DrSipn2+r*Qminus1*(96*DrSipn2-23*Qminus1))+eta*(-54+r*(120*Qminus1-324*DrSipn2+r*(360*DrSipn2*DrSipn2+Qminus1*(-126*DrSipn2-3*Qminus1))))) ;
    REAL8 sigmastarcoeffTerm1=eta/12*(14/r+4*Qminus1-30*DrSipn2) ;
    REAL8 sigmastarcoeff=sigmastarcoeffTerm1+sigmastarcoeffTerm2 ;
    REAL8 Deltasigmastar3=sigmastar3*sigmastarcoeff+sigma3*sigmacoeff ;
    REAL8 Deltasigmastar2 = sigmastar2*sigmastarcoeff + sigma2*sigmacoeff ;
    REAL8 Deltasigmastar1 = sigmastar1*sigmastarcoeff + sigma1*sigmacoeff ;
    REAL8 Sstar3=sigmastar3+Deltasigmastar3 ;
    REAL8 Sstar2 = sigmastar2 + Deltasigmastar2 ;
    REAL8 Sstar1 = sigmastar1 + Deltasigmastar1 ;
    REAL8 S3 = Sstar3 ;
    REAL8 S2 = Sstar2 ;
    REAL8 S1 = Sstar1 ;
    REAL8 Sstardotn=Sstar1*n1+Sstar2*n2+Sstar3*n3 ;
    REAL8 SdotSkerrhat=S1*Skerrhat1+S2*Skerrhat2+S3*Skerrhat3 ;
    REAL8 Sdotn=S1*n1+S2*n2+S3*n3 ;
    REAL8 Sdotv=S1*v1+S2*v2+S3*v3 ;
    REAL8 Sdotxi=S1*xi1+S2*xi2+S3*xi3 ;
    REAL8 HdsumTerm2=3*Sstardotn*Sstardotn ;
    REAL8 HdsumTerm1=Sstar1*Sstar1+Sstar2*Sstar2+Sstar3*Sstar3 ;
    REAL8 Hdsum=HdsumTerm1-HdsumTerm2 ;
    REAL8 Hdcoeff=np.divide(1,2)/(r*r*r) ;
    REAL8 Q4=2*prT*prT*prT*prT*u*u*(4-3*eta)*eta ;
    REAL8 gammappsum=Deltar/Sigma*pdotn*pdotn+1/Sigma*pdotvr*pdotvr/sin2theta+Sigma/Lambdat/sin2theta*pdotxir*pdotxir ;
    REAL8 Hnsradicand=1+gammappsum+Q4 ;
    REAL8 alpha=sqrt(Deltat*Sigma/Lambdat) ;
    REAL8 betapsum=omegatilde*pphi/Lambdat ;
    REAL8 HssTerm3=expmu*expnu*pdotxir*(Jtilde*pdotn*Sdotxi*Btilde-expmu*expnu*pdotxir*Sdotn)+(pdotvr*(Sdotn*pdotvr-Jtilde*pdotn*Sdotv)-exp2mu*(sqrt(Q)+Q)*Sdotn*xisq)*Btilde*Btilde ;
    REAL8 HssTerm3coeff=omegacostheta/(2*exp2mu*expmu*expnu*Btilde*(Q+sqrt(Q))) ;
    REAL8 HssTerm2=expmu*pdotxir*(expmu*exp2nu*pdotxir*Sdotv-expnu*pdotvr*Sdotxi*Btilde)+xisq*Btilde*Btilde*(exp2mu*(sqrt(Q)+Q)*Sdotv+Jtilde*pdotn*(pdotvr*Sdotn-Jtilde*pdotn*Sdotv)) ;
    REAL8 HssTerm2coeff=Jtilde*omegar/(2*exp2mu*expmu*expnu*Btilde*(Q+sqrt(Q))*xisq) ;
    REAL8 HssTerm1=omega*SdotSkerrhat ;
    REAL8 Hss=HssTerm1+HssTerm2coeff*HssTerm2+HssTerm3coeff*HssTerm3 ;
    REAL8 HsoTerm2c=Jtilde*Brtilde*expmu*expnu*pdotxir*(sqrt(Q)+1)*Sdotv ;
    REAL8 HsoTerm2b=expmu*expnu*pdotxir*(2*sqrt(Q)+1)*(Jtilde*nur*Sdotv-nucostheta*Sdotn*xisq)*Btilde ;
    REAL8 HsoTerm2a=Sdotxi*Jtilde*(mur*pdotvr*(sqrt(Q)+1)-mucostheta*pdotn*xisq-sqrt(Q)*(nur*pdotvr+(mucostheta-nucostheta)*pdotn*xisq))*Btilde*Btilde ;
    REAL8 HsoTerm2=HsoTerm2a+HsoTerm2b-HsoTerm2c ;
    REAL8 HsoTerm2coeff=expnu/(exp2mu*Btilde*Btilde*(Q+sqrt(Q))*xisq) ;
    REAL8 HsoTerm1=exp2nu*(expmu*expnu-Btilde)*pdotxir*SdotSkerrhat/(expmu*Btilde*Btilde*sqrt(Q)*xisq) ;
    REAL8 Hso=HsoTerm1+HsoTerm2coeff*HsoTerm2 ;
    REAL8 Hd=Hdcoeff*Hdsum ;
    REAL8 Hns=betapsum+alpha*sqrt(Hnsradicand) ;
    REAL8 Hs=Hso+Hss ;
    REAL8 dSS=528.511*chi*chi*chi*eta*eta-41.0003*chi*chi*chi*eta+1161.78*chi*chi*eta*eta*eta-326.325*chi*chi*eta*eta+37.1964*chi*eta+706.958*eta*eta*eta-36.0272*eta+6.06807 ;
    REAL8 Heff = Hs + Hns - Hd + dSS*eta*u*u*u*u*(S1x*S1x + S1y*S1y + S1z*S1z + S2x*S2x + S2y*S2y + S2z*S2z) ;
    REAL8 Hreal=sqrt(1+2*eta*(Heff-1)) ;
    return Hreal;
}